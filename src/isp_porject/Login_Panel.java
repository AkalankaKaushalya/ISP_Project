    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String username = jTextField1.getText();
        String password = jTextField2.getText();
        if(username.isEmpty())
        {
            JOptionPane.showMessageDialog(rootPane, "Enter User Name");
        }
        else if (password.isEmpty())
        {
            JOptionPane.showMessageDialog(rootPane, "Enter Password");
        }
        else
        {
            try {
                String q = "SELECT * FROM `employee` WHERE `username`=? AND `password`=?";
                pst = conn.prepareStatement(q);
                pst.setString(1, username);
                pst.setString(2, password);
                rs = pst.executeQuery();
    
                if (rs.next()) {
                    String role = rs.getString("role");
                    ImageIcon icon = new ImageIcon("src/icon/ok.png");
                    JOptionPane.showMessageDialog(null, "Login successful", "Successfully", JOptionPane.INFORMATION_MESSAGE, icon);
                    if (role.equals("staff")) {
                        Staff_Dashbord staff = new Staff_Dashbord();
                        staff.setVisible(true);
                        this.dispose();
                    } else if (role.equals("manager")) {
                        Manager_Dashbord maneger = new Manager_Dashbord();
                        maneger.setVisible(true);
                        this.dispose();
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Email and password not registered", "Error", JOptionPane.ERROR_MESSAGE);
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

